# Description: Operators in R

# Vector Data
x <- c(1, 2, 3, 4, 5)
print(x)

# Mathematical Operators
result <- x + 1           # The addition operator +
result <- x - 1           # The subtraction operator -
result <- x * 2           # The multiplication operator *
result <- x / 2           # The division operator /
result <- x ^ 2           # The power or exponentiation operator ^

# Print result
print(result)

