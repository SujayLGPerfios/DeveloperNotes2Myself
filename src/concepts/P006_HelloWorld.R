# Description: 'Hello World' in R

# Note
# There are 2 ways to print messages in R.
# - Implicit printing: Explicit printing is used while writing scripts, functions, or longer programs because
#   auto-printing does not work in those settings.
# - Explicit printing: Implicity printing or auto-printing is typically used with interactive work where it is easier
#   to print by just typing the the name of the object.

# Explicit printing using the print function.
print('Hello World') 

# Implicit printing by just typing 'Hello World' in console.
'Hello World'

